<?php
//====================================================================
//====================================================================
//====================================================================
//
// DS-Portal 用 Widget 基本クラス
//
//====================================================================
//====================================================================
//====================================================================
class CParts
{
	//================================================================
	// コンストラクタ
	//================================================================
	public function __construct( $strUser, $aryConfig )
	{
		$this->m_aryConfig = $aryConfig;
		$this->m_strUser = $strUser;
	}

	//================================================================
	// メンバー変数
	//================================================================
	public $m_aryConfig = array( );
	public $m_strUser   = '';

	//================================================================
	// 実行
	//================================================================
	public function Exec( )
	{
		$this->Init( );
		$this->Run( );
	}

	//================================================================
	//! 初期化
	//! @param  
	//! @return 
	//! @retval 
	//================================================================
	public function Init( )
	{
	}

	//================================================================
	//! 実行
	//! @param  
	//! @return 
	//! @retval 
	//================================================================
	public function run( )
	{
		//=== ヘッダー出力 ===========================================
		$this->EchoHead( );

		//=== ボディー出力 ===========================================
		$this->EchoBodyStart( );
		$this->EchoBodyContents( );
		$this->EchoBodyEnd( );
	}

	//================================================================
	//=== ヘッダーの出力 =============================================
	//================================================================
	public function EchoHead( )
	{
?>
<head>
<style type="text/css">
</style>

<script type="text/javascript">
jQuery(function($){
});
</script>
</head>
<?php
	}

	//================================================================
	//! <body></body> の開始
	//================================================================
	public function EchoBodyStart( )
	{
?>
<body>
<?php
	}

	//================================================================
	//! <body></body> の中身の出力
	//================================================================
	public function EchoBodyContents( )
	{
?>
<!--===============================================================-->
<div class="contents">
<?php

?>
</div><!--contents-->
<?php
	}

	//================================================================
	//! <body></body> の終了
	//================================================================
	public function EchoBodyEnd( )
	{
?>
</body>
<?php
	}
}
?>